                    <div class="column">
                        <a class="faq" id="1"><b>1</b> What is the legal basis of your rescue missions?</a>
                        <div class="faq-klappe" id="1-klappe">
                            <p>The legal basis for our rescue missions in the Mediterranean is the „United Nations Convention on the Law of the Sea“, UNCLOS of December 10th 1982:  “Every State shall require the master of a ship flying its flag, in so far as he can do so without serious danger to the ship, the crew or the passengers: (a) to render assistance to any person found at sea in fatal danger.“<sup>1</sup></p>
                        </div><br>
                        <a class="faq" id="2"><b>2</b> Who do you rescue?</a>
                        <div class="faq-klappe" id="2-klappe">
                            <p>We rescue people whose lives are in danger on the Mediterranean. Who these rescued people are is irrelevant as every human deserves to be rescued on the basis of human rights.</p>
                        </div><br>
                        <a class="faq" id="3"><b>3</b> How do you rescue these people?</a>
                        <div class="faq-klappe" id="3-klappe">
                            <p>The coordination of the rescue operations in the Mediterranean is conducted by the Maritime Rescue Coordination Centre, situated in Rome. We are informed about ships in distress via radio or we report back if we sight a ship in distress.<sup>2</sup> Subsequently we will approach the endangered vessel and enquire the number of pregnant women and children on board, as they have priority in being rescued. We take everyone on board and provide them with food, drinking water and  medical first aid.</p>
                        </div><br>
                        <a class="faq" id="4"><b>4</b> Where will your ship be operating?</a>
                        <div class="faq-klappe" id="4-klappe">
                            <p>Our Ship will be patrolling on the Mediterranean between Libya and Italy. The Sea route is deemed a central Mediterranean route, through which the populace of refugees attempt to reach Europe.</p>
                        </div><br>
                        <a class="faq" id="5"><b>5</b> Where do the people who escape via the central Mediterannean route come from?</a>
                        <div class="faq-klappe" id="5-klappe">
                            <p>The majority of people comes from Syria, Eritrea and Sub-Saharan Africa.<sup>3</sup></p>
                        </div><br>
                        <a class="faq" id="6"><b>6</b> Where do you take the people after the rescue?</a>
                        <div class="faq-klappe" id="6-klappe">
                            <p>According to the 1979 ratified International Convention on Maritime Search and Rescue, we are obliged to localize the people in a secure place. The criteria for a secure place are to provide for food, accommodation and medical care.<sup>4</sup> Furthermore there may not be a danger of persecution.<sup>5</sup> Based on the instructions of the MRCC we'll bring the rescued people to an Italian harbour.</p>
                        </div><br>
                        <a class="faq" id="7"><b>7</b> Why would you take over the rescue operations?</a>
                        <div class="faq-klappe" id="7-klappe">
                            <p>Approximately 3500 People died on the Mediterranean in 2014. There has not been a universal state rescue program on the Mediterranean since the end of Mare Nostrum. Instead came Frontex with its Mission Triton mission, who is ostensibly responsible for securing the external borders of Europe.<sup>6</sup> As there has not been any sufficient rescue efforts on behalf of the state, we are compelled and feel urged to become involved.</p>
                        </div><br>
                        <a class="faq" id="8"><b>8</b> What is Mare Nostrum?</a>
                        <div class="faq-klappe" id="8-klappe">
                            <p>Mare Nostrum was the Italian Sea rescue operation which was conducted by the Italian Marine. It came into existence in response to the accident that occurred close to the Isle of Lampedusa in which 360 people from Somalia and Eritrea died. In only one year 130.000 people, who were escaping, were rescued through these Operations. The Rescue missions took place also on Libyan waters. Due to a lack of financial support from other European States, the monthly 9 million euro costing program was halted by the Italian government.<sup>7</sup></p>
                        </div><br>
                        <a class="faq" id="9"><b>9</b> What is Frontex?</a>
                        <div class="faq-klappe" id="9-klappe">
                            <p>Frontex is a european border protection agency with its headquarters in Warsaw, whose task is to coordinate the operations on external EU-borders. An important goal of the Agency is to combat the illegal immigration through external EU borders.<sup>8</sup></p>
                        </div><br>
                        <a class="faq" id="10"><b>10</b> What is Triton?</a>
                        <div class="faq-klappe" id="10-klappe">
                            <p>Triton is the currently running Frontex operation to secure the borders on the Italian coast, in place since November 2014. Ostensibly rescue missions are invested in with a third of the budget allocated for Mare Nostrum. The focus of the operation is the control of external European borders. After another catastrophe on the Mediterranean, the allocated budget to triton was raised to 9 million euros monthly. The Operating space is just like before restricted to the vicinity of the Italian coast.<sup>9</sup></p>
                        </div><br>
                        <a class="faq" id="11"><b>11</b> What does the EU do to improve the situation in the Mediterranean?</a>
                        <div class="faq-klappe" id="11-klappe">
                            <p>Early in the year of 2015 the EU decided in a special meeting for the EUNAVFOR Med Operation. The objective of the operation is to identify and destroy human trafficking networks. “The Operation plan is mainly devised to find the ships of traffickers, to raise and confiscate them. In the third phase all necessary measures against traffickers, particularly their boats and facilities, are taken, even on foreign territory.”<sup>10</sup> As before there has not been a Sea rescue program from the EU.</p>
                        </div><br>
                        <a class="faq" id="12"><b>12</b> What does the German government do in order to improve the situation in the Mediterranean?</a>
                        <div class="faq-klappe" id="12-klappe">
                            <p>In May 2015 Germany sent two Marine ships to the Mediterranean with the task of sea rescue mission. Since the 30th of July both ships belong to the EU Mission EUNAVFOR Med and are officially not anymore “only” for rescuing people in danger on the seas, but in operation above all else for objective combatting of human trafficking.<sup>11</sup></p>
                        </div><br>
                        <a class="faq" id="13"><b>13</b> What other private initiatives also operate rescue missions in the Mediterranean?</a>
                        <div class="faq-klappe" id="13-klappe">
                            <p>Sea-Watch, MOAS and Doctors Without Borders are currently active as private initiatives in the Mediterranean sea.</p>
                        </div><br>
                        <a class="faq" id="14"><b>14</b> Aren't you supporting smugglers with your actions?</a>
                        <div class="faq-klappe" id="14-klappe">
                            <p>The lack of legal routes into Europe is forcing humans to trust smugglers with their lives. That way, criminals can run a profitable business on their despair. What kind of distress is someone going through to put themselves on a tiny dinghy across the Mediterranean Sea?
We are convinced that only long-run improvements in the regions of origin will lead to less refugees on the Mediterranean. As long as improvements aren't implemented, smugglers will make a profit out of the hopeless situation of refugees, irrespective of rescue missions in place.</p>
                        </div><br>
                        <a class="faq" id="15"><b>15</b> What’s the difference between you and Sea Watch?</a>
                        <div class="faq-klappe" id="15-klappe">
                            <p>The most important difference is, that we want to use our young generation’s potential and therefore aim at young people. Secondly, we will take people in distress on board ourselves. Sea Watch uses life rafts and is dependent on other ships to take the people on board. The final difference is that due to our ship’s size we are able to operate the whole year.</p>
                        </div><br>
                        <a class="faq" id="16"><b>16</b> Why is the youth aspect so important to you?</a>
                        <div class="faq-klappe" id="16-klappe">
                            <p>With our organization we show that young people are able to move something without a big umbrella organization in the back. But we do not only speak to young adults as their possibilities to change world problems are often underestimated. Furthermore the youth aspect is making us stronger regarding our politcal demand: If even us young people manage to finance a ship, why doesn’t the German governmanet manage so?</p>
                        </div><br>
                        <a class="faq" id="17"><b>17</b> Why is in a time like that, a ship the on the Mediterranean most important?</a>
                        <div class="faq-klappe" id="17-klappe">
                            <p>The deaths on the Mediterranean central route are vanished from the headlines, but the situation hasn’t. The number of refugees and deaths are as high as ever and therefore the distance between Libya and Italy stays the most dangerous escape way. We don’t let the media tell us that all the refugees have arrived and that the only important thing now is their integration. We engage ourselves now to be there in 2016 before the next message about a big disaster on the Mediterranean appears.</p>
                        </div>
