                        <p>
                            <i>Jugend Rettet</i> nimmt seine Aufgaben als vertrauenswürdige und 
                            verantwortungsbewusste gemeinnützige Organisation ernst. Transparenz ist uns 
                            wichtig. Deshalb wollen wir uns der <a target="_blank" href="https://www.transparency.de/Initiative-Transparente-Zivilg.1612.0.html"><u>Initiative Transparente Zivilgesellschaft</u></a> 
                            anschließen. Wir haben uns in diesem Rahmen verpflichtet, die nachstehenden 
                            Informationen der Öffentlichkeit auf unserer Webseite zur Verfügung zu stellen und 
                            regelmäßig zu updaten.
                        </p>  
                        <h4>1. NAME, SITZ, ANSCHRIFT UND GRÜNDUNGSJAHR</h4>
                        <p>
                            Der Verein <i>Jugend Rettet</i> e.V. wurde am 3. Oktober 2015 gegründet. Sitz und Anschrift 
                            des Vereins befinden sich in der Ruhlsdorferstraße 151, 14513 Teltow.
                        </p>
                        <h4>2. SATZUNG, ZIELE UND UMSETZUNG</h4>
                        <p>
                            Unsere Vereinssatzung in ihrer aktuellen von der ordentlichen Mitgliederversammlung 
                            am 03. Oktober 2015 in Berlin beschlossenen Fassung finden Sie <a target="_blank" href="http://jugendrettet.org/files/Vereinssatzung_v1.pdf">hier</a>.
                        </p>
                        <br>
                        <p>
                            Eine Übersicht zu den Zielen und Umsetzung des Vereins finden Sie <a target="_blank" href="schiff">hier</a>.
                        </p>
                        <h4>3. ANGABEN ZUR STEUERBEGÜNSTIGUNG</h4>
                        <p>
                            Die Satzung von „Jugend Rettet e.V.“ erfüllt nach dem letzten uns zugegangenen
                            Bescheid des Finanzamts für Körperschaften I, Berlin (Steuernummer 27/669/53338) 
                            vom 03.11.2015 nach § 5 Abs. 1 Nr. 9 die offiziellen Kriterien zur Förderung folgender 
                            gemeinnütziger Zwecke:
                        </p>
                        <ul>
                            <li>Förderung der Hilfe für Flüchtlinge und Förderung der Rettung aus Lebensgefahr nach § 52 Abs. 2 Satz 1 Nr.(n) 10 und 11 AO</li>
                            <li>Förderung der Internationalen Gesinnung und Förderung des Bürgerschaftlichen Engagements nach § 52 Abs. 2 Satz 1 Nr.(n) 13 und 25 AO</li>
                        </ul>
                        <p>Folglich ist unsere Organisation von der Körperschaftssteuer und Gewerbesteuer befreit.</p>
                        <br>
                        <p>Der <i>Jugend Rettet</i> e.V. ist berechtigt Spendenbescheinigungen auszustellen.</p>
                        <br>
                        <p>Den offiziellen Bescheid finden Sie <a target="_blank" href="http://jugendrettet.org/files/Gemeinnuetzigkeit_Bescheid.pdf">hier</a>.</p>
