                        </p>

                        <h4>9. GESELLSCHAFTSRECHTLICHE VERBUNDENHEIT MIT DRITTEN</h4>
                        <p>
                            Eine gesellschaftsrechtliche Verbundenheit mit Dritten, z.B. ausgegliederter
                            Wirtschaftsbetrieb, Partnerorganisation oder Ähnliches besteht nicht.
                        </p>
                        <h4>10. NAMEN VON JURISTISCHEN PERSONEN, DEREN JÄHRLICHE ZAHLUNGEN MEHR ALS 10% DES GESAMTBUDGETS AUSMACHEN</h4>
                        <p>
                            Jugend Rettet hat derzeit keine Unterstützer oder Förderer, deren jährliche Zahlungen 
                            mehr als 10% des Gesamtbudgets ausmachen. Unsere Arbeit trägt sich durch das 
                            Engagement und die Hilfe vieler.
