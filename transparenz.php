                        <p>
                            Eine Übersicht über die Tätigkeiten des Vereins finden Sie in unserem <a target="_blank" href="http://jugendrettet.org/files/Jahresbericht_2015.pdf">Jahresbericht 2015</a>.
                        </p>
                        <h4>6. PERSONALSTRUKTUR</h4>
                        <p>
                            Alle an Jugend Rettet beteiligten Personen, wie auch alle Berater arbeiten ausschließlich 
                            auf ehrenamtlicher Basis. Unser Team besteht aus überwiegend jungen Menschen, 
                            deren Motivation für das Projekt auf das Retten auf einer Verbesserung der 
                            humanitären Lage im Mittelmeer basiert.
                        </p>
                        <br>
                        <p>
                            Das Jugend Rettet Kernteam in Berlin besteht aus 9 Leuten. Deutschlandweit engagieren 
                            sich derzeit 60 Personen an unserer Arbeit.
                        </p>
