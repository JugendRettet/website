                        <h4><img class="icon" src="http://jugendrettet.org/graphics/ide.svg" alt="Ideal">Ideal</h4>
                        <p>Wir wollen ein Ende der Gleichgültigkeit der Europäischen Gemeinschaft gegenüber dem Sterben auf dem Mittelmeer.</p>
                        <div class="mehr-klappe"><br>
                            <p>Wir setzen uns für eine humanere, gerechtere und engagiertere Asylpolitik ein.</p>
                            <p>Menschen auf der Flucht unterscheiden sich von uns nur durch die Notsituation, in die sie unverschuldet geraten sind.  Ihr Recht auf Menschenwürde gilt für sie uneingeschränkt, auch während der Flucht. Um das zu erreichen, wollen und müssen wir Verantwortung übernehmen.</p>
                            <p>Junge Erwachsene sollen ihr Potenzial nutzen und sich politisch engagieren. Jeder kann etwas ändern. Als moderne Europäer vertrauen wir dabei in die Kraft eines vereinten europäischen Engagements, um gemeinsam für mehr Menschlichkeit in der Asylpolitik zu sorgen.</p>
